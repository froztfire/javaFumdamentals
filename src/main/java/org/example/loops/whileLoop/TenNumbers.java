package org.example.loops.whileLoop;

//Display the numbers from 1 to 10 using a while loop. Each value should be on a new line.
class TenNumbers {
    public static void main(String[] args) {
        int i = 0;
        while (i != 10){
            i++;
            System.out.println("i = " + i);
        }
    }
}

